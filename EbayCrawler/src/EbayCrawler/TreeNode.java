package EbayCrawler;

import javax.swing.tree.DefaultMutableTreeNode;

//A tree node class for storing the categories
public class TreeNode extends DefaultMutableTreeNode{
   String name;
   String id;
   String timeStamp;
   TreeNode(String name,String id){
	   this.name=name;
	   this.id=id;
   }
   TreeNode(String name,String id, String timeStamp){
	   this.name=name;
	   this.id=id;
	   this.timeStamp=timeStamp;
   }
   public String toString(){
	   return name;
   }
}
